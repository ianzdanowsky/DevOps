myDict = {'Ian': 100, 'Biscoito': 200, 'Paloma': 300}

print(list(myDict))        # Turn the keys into a list

print(myDict.keys())        # Retrieve only the keys of a dictionary

print(myDict.values())      # Retrieve only the values of the dictionary keys

print(myDict.items())       # Retrieve 3 itens list in a tuple format